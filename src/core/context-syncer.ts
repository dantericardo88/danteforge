// ── Main sync function ────────────────────────────────────────────────────────

export async function syncContext(options: ContextSyncOptions = {}): Promise<ContextSyncResult> {
  const cwd = options.cwd ?? process.cwd();
  const rawTarget = options.target ?? 'all';

  const targets: Array<Exclude<ContextTarget, 'all'>> =
    rawTarget === 'all'
      ? ['cursor', 'claude', 'codex']
      : [rawTarget as Exclude<ContextTarget, 'all'>];

  const writeFile = options._writeFile
    ?? (async (p: string, content: string) => {
      const { writeFile: fsWrite } = await import('fs/promises');
      await fsWrite(p, content, 'utf8');
    });

  const mkdir = options._mkdir
    ?? (async (p: string, opts?: { recursive?: boolean }) => {
      const { mkdir: fsMkdir } = await import('fs/promises');
      await fsMkdir(p, opts);
    });

  const targetPaths: Record<Exclude<ContextTarget, 'all'>, string> = {
    cursor: path.join(cwd, '.cursor', 'rules', 'danteforge-project.mdc'),
    claude: path.join(cwd, 'CLAUDE.local.md'),
    codex: path.join(cwd, 'AGENTS.local.md'),
  };

  const synced: SyncedContextFile[] = [];
  const skipped: string[] = [];

  for (const target of targets) {
    try {
      let content: string;
      if (target === 'cursor') {
        content = await buildCursorContext(cwd, options);
      } else if (target === 'claude') {
        content = await buildClaudeContext(cwd, options);
      } else {
        content = await buildCodexContext(cwd, options);
      }

      const filePath = targetPaths[target];
      const parentDir = path.dirname(filePath);

      try {
        await mkdir(parentDir, { recursive: true });
      } catch {
        // best-effort — directory may already exist
      }

      await writeFile(filePath, content);

      const tokensEstimated = estimateTokens(content);
      synced.push({ target, path: filePath, content, tokensEstimated });
    } catch {
      skipped.push(target);
    }
  }

  const totalTokens = synced.reduce((sum, f) => sum + f.tokensEstimated, 0);

  return { synced, skipped, totalTokens };
}
