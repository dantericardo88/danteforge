// Main export
// ---------------------------------------------------------------------------

/**
 * Run the first-time onboarding wizard.
 * Safe to call multiple times — exits immediately if already initialized.
 */
export async function runOnboardingWizard(
  cwd: string,
  options: OnboardingOptions = {},
): Promise<void> {
  const write = options._writeFn ?? defaultWrite;
  const configDir = options._configDir ?? path.join(os.homedir(), '.danteforge');

  if (!isFirstRun(cwd, configDir)) {
    return; // already initialized — nothing to do
  }

  write('');
  write('╔══════════════════════════════════════════════════════╗');
  write('║   Welcome to DanteForge — First-Run Setup Wizard    ║');
  write('╚══════════════════════════════════════════════════════╝');
  write('');
  write('DanteForge needs a few details to set up your project.');

  // Require a readline fn — callers must provide real readline or a test double
  const readlineFn = options._readlineFn;
  if (!readlineFn) {
    // Non-interactive fallback: write instructions and exit
    write('');
    write('Run `danteforge init` to complete setup interactively.');
    write('');
    return;
  }

  const projectName = await askQuestion('\nProject name (or press Enter to use folder name): ', readlineFn);
  const resolvedName = projectName || path.basename(cwd);

  const state: OnboardingState = {
    projectName: resolvedName,
    projectType: await selectProjectType(readlineFn, write),
    provider: await selectProvider(readlineFn, write),
  };

  write('\nSetting up your project...');

  // Write global config
  const configPath = path.join(configDir, 'config.yaml');
  const wroteConfig = writeFileIfMissing(configPath, CONFIG_TEMPLATE(state.provider));
  if (wroteConfig) {
    write(`  Created global config at ${configPath}`);
  }

  // Write CONSTITUTION.md
  const constitutionPath = path.join(cwd, 'CONSTITUTION.md');
  const wroteConstitution = writeFileIfMissing(
    constitutionPath,
    CONSTITUTION_TEMPLATE(state.projectName, state.projectType),
  );
  if (wroteConstitution) {
    write(`  Created ${constitutionPath}`);
  }

  // Initialize .danteforge directory
  const danteforgeDir = path.join(cwd, '.danteforge');
  ensureDir(danteforgeDir);

  write('');
  write('Setup complete! Next steps:');
  write('');
  for (const step of NEXT_STEPS) {
    write(step);
  }
  write('');
}
