const NEXT_STEPS = [
  '1. Run `danteforge constitution` to refine your CONSTITUTION.md',
  '2. Run `danteforge specify` to write your project spec',
  '3. Run `danteforge plan` to generate an implementation plan',
  '4. Run `danteforge tasks` to break the plan into tasks',
  '5. Run `danteforge forge` to execute the first wave',
  '',
  'Tip: Run `danteforge flow` anytime to see the workflow decision tree.',
  'Tip: Run `danteforge go` for the daily driver experience.',
];

// ---------------------------------------------------------------------------
// Core wizard logic
// ---------------------------------------------------------------------------

function defaultWrite(msg: string): void {
  process.stdout.write(msg + '\n');
}

async function askQuestion(
  prompt: string,
  readlineFn: (p: string) => Promise<string>,
): Promise<string> {
  return (await readlineFn(prompt)).trim();
}

async function selectProjectType(
  readlineFn: (p: string) => Promise<string>,
  write: (msg: string) => void,
): Promise<ProjectType> {
  write('\nWhat type of project is this?');
  write('  1) Web app');
  write('  2) CLI tool');
  write('  3) Library / package');
  write('  4) Monorepo');
  write('  5) Other');
  const answer = await askQuestion('Enter number [1-5]: ', readlineFn);
  const map: Record<string, ProjectType> = {
    '1': 'web-app',
    '2': 'cli',
    '3': 'library',
    '4': 'monorepo',
    '5': 'other',
  };
  return map[answer] ?? 'other';
}

async function selectProvider(
  readlineFn: (p: string) => Promise<string>,
  write: (msg: string) => void,
): Promise<ProviderChoice> {
  write('\nWhich LLM provider do you want to use?');
  write('  1) Ollama  (local, free — recommended for first time)');
  write('  2) Claude  (Anthropic API key required)');
  write('  3) OpenAI  (API key required)');
  write('  4) Grok    (xAI API key required)');
  write('  5) Gemini  (Google API key required)');
  const answer = await askQuestion('Enter number [1-5]: ', readlineFn);
  const map: Record<string, ProviderChoice> = {
    '1': 'ollama',
    '2': 'claude',
    '3': 'openai',
    '4': 'grok',
    '5': 'gemini',
  };
  return map[answer] ?? 'ollama';
}

// ---------------------------------------------------------------------------
// File creation helpers (best-effort, no throws)
// ---------------------------------------------------------------------------

function ensureDir(dir: string): void {
  try {
    fs.mkdirSync(dir, { recursive: true });
  } catch {
    // best-effort
  }
}

function writeFileIfMissing(filePath: string, content: string): boolean {
  if (fs.existsSync(filePath)) return false;
  try {
    ensureDir(path.dirname(filePath));
    fs.writeFileSync(filePath, content, 'utf8');
    return true;
  } catch {
    return false;
  }
}

