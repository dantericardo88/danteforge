// First-run onboarding wizard — guides new users through DanteForge setup
// Detects missing STATE.yaml / config.yaml and walks through project setup.

import fs from 'node:fs';
import path from 'node:path';
import os from 'node:os';

// ---------------------------------------------------------------------------
// Types & injection seams
// ---------------------------------------------------------------------------

export type ProjectType = 'web-app' | 'cli' | 'library' | 'monorepo' | 'other';
export type ProviderChoice = 'ollama' | 'claude' | 'openai' | 'grok' | 'gemini';

export interface OnboardingOptions {
  /** Inject a custom readline fn for testing. Receives a prompt and returns the answer. */
  _readlineFn?: (prompt: string) => Promise<string>;
  /** Inject a custom writer for testing (defaults to process.stdout.write). */
  _writeFn?: (msg: string) => void;
  /** Override global config directory (defaults to ~/.danteforge). */
  _configDir?: string;
}

interface OnboardingState {
  projectType: ProjectType;
  provider: ProviderChoice;
  projectName: string;
}

// ---------------------------------------------------------------------------
// First-run detection
// ---------------------------------------------------------------------------

/** Returns true if the project has not been initialized yet. */
export function isFirstRun(cwd: string, configDir?: string): boolean {
  const stateFile = path.join(cwd, '.danteforge', 'STATE.yaml');
  const globalConfig = path.join(configDir ?? path.join(os.homedir(), '.danteforge'), 'config.yaml');
  return !fs.existsSync(stateFile) && !fs.existsSync(globalConfig);
}

/** Returns true if CONSTITUTION.md is missing in the project. */
export function isConstitutionMissing(cwd: string): boolean {
  return !fs.existsSync(path.join(cwd, 'CONSTITUTION.md'));
}

// ---------------------------------------------------------------------------
// Template generators
// ---------------------------------------------------------------------------

const CONSTITUTION_TEMPLATE = (projectName: string, projectType: ProjectType): string => `# Constitution — ${projectName}

## Purpose
> Describe the core purpose and mission of this project.

## Project Type
${projectType}

## Quality Standards
- Every feature must have tests before code (TDD)
- No stubs or TODO-only implementations shipped to production
- All LLM-generated code is reviewed before merge
- Error messages must be actionable and suggest next steps

## Architecture Principles
- Keep files under 500 lines (hard cap: 750)
- Prefer pure functions with injected dependencies for testability
- Document non-obvious decisions in code comments

## Definition of Done
- [ ] Tests pass (npm run verify)
- [ ] No TypeScript errors (npm run typecheck)
- [ ] Feature works end-to-end in the target environment
- [ ] CHANGELOG entry added
`;

const CONFIG_TEMPLATE = (provider: ProviderChoice): string => `# DanteForge global config
# Generated by onboarding wizard

llmProvider: ${provider}
${provider === 'ollama' ? 'ollamaModel: llama3\nollamaBaseUrl: http://localhost:11434' : '# Add your API key: apiKey: YOUR_KEY_HERE'}
`;

